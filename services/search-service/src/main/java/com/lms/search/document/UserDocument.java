package com.lms.search.document;

import lombok.*;
import org.springframework.data.annotation.Id;
import org.springframework.data.elasticsearch.annotations.Document;
import org.springframework.data.elasticsearch.annotations.Field;
import org.springframework.data.elasticsearch.annotations.FieldType;
import java.util.List;

@Document(indexName = "users")
@Getter @Setter @Builder @NoArgsConstructor @AllArgsConstructor
public class UserDocument {
    @Id private String id;
    @Field(type = FieldType.Text, analyzer = "standard") private String username;
    @Field(type = FieldType.Text, analyzer = "standard") private String email;
    @Field(type = FieldType.Text, analyzer = "standard") private String firstName;
    @Field(type = FieldType.Text, analyzer = "standard") private String lastName;
    @Field(type = FieldType.Keyword) private List<String> roles;
    @Field(type = FieldType.Boolean) private boolean active;
}
